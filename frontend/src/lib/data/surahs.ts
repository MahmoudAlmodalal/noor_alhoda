/** Convert integer to Arabic-Indic numeral string */
export function toArabicNumeral(n: number): string {
  const digits = "٠١٢٣٤٥٦٧٨٩";
  return String(n)
    .split("")
    .map((d) => digits[parseInt(d)] ?? d)
    .join("");
}
